export const createPost = async (req, res, next) => {
  try {
    const { userId } = req.body.user;
    const { description, image } = req.body;

    if (!description) {
      next("You must provide a description");
      return;
    }

    // AI Spam Detection
    try {
      const spamResponse = await axios.post("http://localhost:8000/detect_spam", {
        text: description,
      });
      if (spamResponse.data.is_spam) {
        return res.status(400).json({
          success: false,
          message: "Post detected as spam. Please revise your content.",
        });
      }
    } catch (aiError) {
      console.log("AI Service Error (Spam Detection):", aiError.message);
      // Continue if AI service is down
    }

    const post = await Posts.create({
      userId,
      description,
      image,
    });

    res.status(200).json({
      success: true,
      message: "Post created successfully",
      data: post,
    });
  } catch (error) {
    console.log(error);
    res.status(404).json({ message: error.message });
  }
};

export const getPosts = async (req, res, next) => {
  try {
    const { userId } = req.body.user;
    const { search } = req.body;

    const user = await Users.findById(userId);
    const friends = user?.friends?.map((f) => f._id.toString()) ?? [];
    friends.push(userId);

    const searchPostQuery = {
      $or: [
        {
          description: { $regex: search, $options: "i" },
        },
      ],
    };

    const posts = await Posts.find(search ? searchPostQuery : {})
      .populate({
        path: "userId",
        select: "firstName lastName location profileUrl -password",
      })
      .sort({ _id: -1 });

    let postsRes = posts;

    // AI Smart Feed Ranking
    if (!search && user?.skills?.length > 0) {
      try {
        const aiResponse = await axios.post("http://localhost:8000/smart_feed", {
          user_skills: user.skills,
          posts: posts.map((p) => ({
            _id: p._id,
            description: p.description,
            userId: p.userId,
            createdAt: p.createdAt,
            likes: p.likes,
            comments: p.comments,
            image: p.image,
          })),
        });
        postsRes = aiResponse.data;
      } catch (aiError) {
        console.log("AI Service Error (Smart Feed):", aiError.message);
        // Fallback to basic friend-based filtering
        const friendsPosts = posts?.filter((post) => {
          return friends.includes(post?.userId?._id.toString());
        });
        const otherPosts = posts?.filter(
          (post) => !friends.includes(post?.userId?._id.toString())
        );
        postsRes = friendsPosts?.length > 0 ? [...friendsPosts, ...otherPosts] : posts;
      }
    } else {
      const friendsPosts = posts?.filter((post) => {
        return friends.includes(post?.userId?._id.toString());
      });
      const otherPosts = posts?.filter(
        (post) => !friends.includes(post?.userId?._id.toString())
      );
      postsRes = friendsPosts?.length > 0 ? [...friendsPosts, ...otherPosts] : posts;
    }

    res.status(200).json({
      success: true,
      message: "successfully",
      data: postsRes,
    });
  } catch (error) {
    console.log(error);
    res.status(404).json({ message: error.message });
  }
};

